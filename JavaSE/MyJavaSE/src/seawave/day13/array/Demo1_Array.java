package seawave.day13.array;

public class Demo1_Array {

	/**
	 * 案例演示 数组高级冒泡排序代码 
	 * 
	 * 冒泡:1.次数arr.leng-1  2.每次都是n与n+1比较  3.每比较完一次,在左边就固定了一位
	 * 
	 * 选择:1.次数arr.leng-1  2.每次都是n与其他的轮流比较  3. 每比较完一次,在右边就固定了一位
	 */
	public static void main(String[] args) {
		int[] arr = { 24, 69, 80, 57, 13 };

		bubbleSort(arr);// 冒泡排序

		// selectSort(arr);//选择排序

		print(arr);

	}

	/*
	 * 冒泡排序: 1,返回值类型,void 2,参数列表,int[] arr
	 * 
	 * 第一次:arr[0]与arr[1],arr[1]与arr[2],arr[2]与arr[3],arr[3]与arr[4]比较4次
	 * 第二次:arr[0]与arr[1],arr[1]与arr[2],arr[2]与arr[3]比较3次
	 * 第三次:arr[0]与arr[1],arr[1]与arr[2]比较2次 第四次:arr[0]与arr[1]比较1次
	 */

	public static void bubbleSort(int[] arr) {
		for (int i = 0; i < arr.length - 1; i++) { // 外循环只需要比较arr.length-1次就可以了
			for (int j = 0; j < arr.length - 1 - i; j++) { // -1为了防止索引越界,-i为了提高效率
				if (arr[j] > arr[j + 1]) {
					/*
					 * int temp = arr[j]; arr[j] = arr[j + 1]; arr[j+1] = temp;
					 */
					swap(arr, j, j + 1);
				}
			}
		}
	}

	/*
	 * 打印数组: 1,返回值类型void 2,参数列表int[]arr
	 */
	public static void print(int[] arr) {
		for (int i = 0; i < arr.length; i++) {
			System.out.print(arr[i] + " ");
		}
	}

	/*
	 * 选择排序: 1,返回值类型void 2, 参数列表int[] arr
	 * 
	 * 第一次:arr[0]分别与arr[1-4]比较,比较4次 第二次:arr[1]分别与arr[2-4]比较,比较3次
	 * 第三次:arr[2]分别与arr[3-4]比较,比较2次 第四次:arr[3]与arr[4]比较,比较1次
	 */

	public static void selectSort(int[] arr) {
		for (int i = 0; i < arr.length - 1; i++) { // 只需要比较arr.length-1次
			for (int j = i + 1; j < arr.length; j++) {
				if (arr[i] > arr[j]) {
					/*
					 * int temp = arr[i]; arr[i] = arr[j]; arr[j] = temp;
					 */
					swap(arr, i, j);
				}
			}
		}
	}

	/*
	 * 换位操作: 1,返回值类型,void 2,参数列表int[] arr.int i,int j
	 * 
	 * 如果某个方法,只针对本类使用,不想让其他类使用就可以定义成私有的
	 */

	private static void swap(int[] arr, int i, int j) {
		int temp = arr[i];
		arr[i] = arr[j];
		arr[j] = temp;
		/*
		 * int a =1; int b =2; a =a^b; b= a^b; // a = abb a= a^b;// aba
		 */
	}

}
